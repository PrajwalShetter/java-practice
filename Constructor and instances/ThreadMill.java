class ThreadMill {

    String brand;
    String color;
    int price;
    double speed;
    String type;
    boolean automatic;
    long id;
    String material;
    double weight;
    boolean foldable;

  
    ThreadMill(String brand, String color, int price, double speed, String type,
               boolean automatic, long id, String material, double weight, boolean foldable) {

        this.brand = brand;
        this.color = color;
        this.price = price;
        this.speed = speed;
        this.type = type;
        this.automatic = automatic;
        this.id = id;
        this.material = material;
        this.weight = weight;
        this.foldable = foldable;
    }
}