class AsianPaint {

    String brand;
    String color;
    int price;
    String type;
    boolean waterproof;
    long id;
    String finish;
    double quantity;
    String usage;
    boolean ecoFriendly;

    
    AsianPaint(String brand, String color, int price, String type,
               boolean waterproof, long id, String finish,
               double quantity, String usage, boolean ecoFriendly) {

        this.brand = brand;
        this.color = color;
        this.price = price;
        this.type = type;
        this.waterproof = waterproof;
        this.id = id;
        this.finish = finish;
        this.quantity = quantity;
        this.usage = usage;
        this.ecoFriendly = ecoFriendly;
    }
}