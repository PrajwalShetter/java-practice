class BarBell {

    String brand;
    String color;
    int price;
    double weight;
    String type;
    boolean adjustable;
    long id;
    String material;
    double length;
    boolean rubberGrip;

    
    BarBell(String brand, String color, int price, double weight, String type,
            boolean adjustable, long id, String material, double length, boolean rubberGrip) {

        this.brand = brand;
        this.color = color;
        this.price = price;
        this.weight = weight;
        this.type = type;
        this.adjustable = adjustable;
        this.id = id;
        this.material = material;
        this.length = length;
        this.rubberGrip = rubberGrip;
    }
}