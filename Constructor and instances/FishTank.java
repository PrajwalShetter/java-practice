class FishTank {

    String brand;
    String color;
    int capacity;
    String shape;
    boolean hasFilter;
    double price;
    String material;
    double weight;
    int fishCount;
    boolean hasLight;

    
    FishTank(String brand, String color, int capacity, String shape, boolean hasFilter,
             double price, String material, double weight, int fishCount, boolean hasLight) {

        this.brand = brand;
        this.color = color;
        this.capacity = capacity;
        this.shape = shape;
        this.hasFilter = hasFilter;
        this.price = price;
        this.material = material;
        this.weight = weight;
        this.fishCount = fishCount;
        this.hasLight = hasLight;
    }

}

