class DumbBells {

    String brand;
    String color;
    int price;
    double weight;
    String type;
    boolean adjustable;
    long id;
    String material;
    int quantity;
    boolean rubberCoated;

    
    DumbBells(String brand, String color, int price, double weight, String type,
              boolean adjustable, long id, String material, int quantity, boolean rubberCoated) {

        this.brand = brand;
        this.color = color;
        this.price = price;
        this.weight = weight;
        this.type = type;
        this.adjustable = adjustable;
        this.id = id;
        this.material = material;
        this.quantity = quantity;
        this.rubberCoated = rubberCoated;
    }
}